Advantage script - script that gives advantage for multipate jobs, or citizen actions in random time.
The script is without config, you have the option to edite everything in the code for adding or change as you wish.
The script is built by parts of any Advantage, you have the Advantage name at the start of every "Advantage event", every part is defined well.

